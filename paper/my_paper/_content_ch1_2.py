def write_ch2(doc):
    add_heading(doc, '2  相关技术介绍', level=1)

    add_heading(doc, '2.1  前后端分离架构', level=2)
    add_para(doc, '所谓前后端分离，说到底是把视图和业务这两件本来缠在一起的事强行掰开。前端工程独立运行在浏览器里，'
                  '关心的事情很集中——渲染、交互、还有页面状态；后端则待在服务器一侧，事情同样收得很窄，只剩业务逻辑、数据持久化、'
                  '以及把接口暴露给前端这三件。两边的对话渠道也很统一：业务请求走 RESTful JSON，实时数据走 WebSocket 长连接。'
                  '和传统的服务端渲染相比，这种结构最实在的好处其实是协作和扩展上的——前后端可以各自按自己的节奏迭代，'
                  '同一份接口契约 Web 端能用，往后想再做移动端或者桌面端也能复用。本系统就是顺着这条主流路线搭起来的，前端 Vue 3、后端 Go + Gin。')

    add_heading(doc, '2.2  Vue 3 与前端技术栈', level=2)
    add_para(doc, 'Vue 3 是一套响应式驱动的渐进式前端框架。相较于 Vue 2，它引入了 Composition API、'
                  '基于 Proxy 的新响应式系统，以及对 TypeScript 更完整的支持。'
                  '本系统以前端 Vue 3 为核心，配套使用 Vite、TypeScript、Pinia、Vue Router 和 Axios。'
                  '这里每个工具的职责都比较明确：Vite 负责构建，TypeScript 帮助在编译期拦住低级错误，'
                  'Pinia 管理状态，Vue Router 负责路由与登录拦截，Axios 统一处理 HTTP 请求和 JWT 注入。')

    add_heading(doc, '2.3  Go 语言与 Gin 框架', level=2)
    add_para(doc, '后端这边之所以挑了 Go，最直接的理由还是并发——IM 服务在线高峰时单台机器要扛住成千上万条 WebSocket 长连接，'
                  '如果按"一条连接对应一个 OS 线程"那种老模型来做，光是线程切换的开销就足够把单机能力压下去。'
                  'goroutine 正好绕开了这件事：每条连接交给一个轻量执行单元，怎么切片、怎么调度由 Go 运行时自己管。'
                  '除了并发模型本身，Go 在日常使用上也省心——代码风格被官方工具链管得很死，编译产物又是单二进制，部署起来基本没什么花活。')
    add_para(doc, 'Gin 是 Go 生态里使用最多的 Web 框架。'
                  '它在 net/http 之上做了 Radix Tree 路由优化，'
                  '并提供一套优雅的中间件机制——'
                  '本系统正是借此把 CORS、JWT 鉴权、日志统一塞到中间件层，'
                  '业务 Handler 里几乎看不到模板代码。'
                  'WebSocket 升级请求也走同一套路由系统，'
                  'HTTP 与 WS 在工程上不需要两套基础设施。')

    add_para(doc, 'Go 还有一处常被低估的优点，在于它的工程文化很统一。'
                  'gofmt、go vet、go test 这几件工具放在一起，基本把代码格式、静态检查和测试底线都钉住了。'
                  '这样一来，协作时很少会把时间耗在无谓的风格争论上。对毕业设计这种本来人手和时间都有限的项目来说，这种统一反而很实用。')

    add_heading(doc, '2.4  WebSocket 协议', level=2)
    add_para(doc, '在 WebSocket 出现之前，'
                  '浏览器想做"准实时"通信，'
                  '只能靠 Comet 长轮询或者 HTTP 流——'
                  '前者频繁建连销毁，'
                  '后者难以穿透代理。'
                  'WebSocket 用一次握手把这个老问题彻底解决。')
    add_para(doc, 'RFC 6455 把 WebSocket 定义为基于 TCP 的全双工协议。它跟 HTTP 那种"先请求、再响应"的交互节奏完全是两种工作方式——'
                  '一次握手过后连接会一直挂着，两端任何一方都可以随时往对面推数据，因此它对上 IM、在线协作、推送通知这一类场景非常自然。'
                  '协议设计上还有一个挺取巧的安排：握手阶段直接复用 HTTP，通过 "Upgrade: websocket" 这个请求头把 HTTP 连接顶到 WS 协议上去，'
                  '握手成功之后的字节流就跟 HTTP 没什么关系了。')
    add_para(doc, '在本系统里，WebSocket 承担的就是实时下行那条通道。客户端通过 GET /ws?token=XXX 把 JWT 带上去发起握手，'
                  '服务端验签通过之后再把这条连接升级成 WebSocket，并丢进统一的 Hub 里登记。'
                  '一旦有新消息进来，Hub 就以接收方的用户 ID 为键查出当前活跃的那条连接、把密文推过去。'
                  'WebSocket 的具体实现选了 Gorilla WebSocket——它在 Go 生态里基本算事实标准，比标准库要稳定。')

    add_heading(doc, '2.5  JWT 身份认证', level=2)
    add_para(doc, 'JWT（JSON Web Token）由 RFC 7519 定义，'
                  '是一种紧凑、自包含的令牌格式，'
                  '在前后端分离系统里通常用来替代 Session。'
                  '它由 Header、Payload、Signature 三段组成，'
                  '中间用点号连接并做 Base64URL 编码。'
                  'Payload 里可以塞用户 ID、过期时间等业务字段，'
                  'Signature 则保证整串令牌没被篡改。')
    add_para(doc, 'JWT 比 Session 更有吸引力的一点在于：服务端不需要再维护会话状态。'
                  '这对横向扩展尤其友好；'
                  '同一份令牌天然可以在 Web 与移动端等多客户端之间通用；'
                  '如果把业务上下文写进 Payload，连一次数据库查询都能省下。'
                  '本系统在登录成功后由后端签发 JWT，前端把它缓存在 localStorage，'
                  'Axios 拦截器自动加到请求头上，后端中间件统一做校验。'
                  'WebSocket 握手时则改走 URL 查询参数，鉴权口径保持一致。')

    add_heading(doc, '2.6  RSA-OAEP 公钥加密与 Web Crypto API', level=2)
    add_para(doc, 'RSA 在非对称加密里属于使用时间最长、覆盖最广的那种算法，1977 年由 Rivest、Shamir、Adleman 这三个人提出，'
                  '安全性靠的是一个相对粗暴的假设——大整数分解在算力上很难。它真正巧妙的不是算法本身，'
                  '而是那对密钥之间的关系：用公钥能加密、用私钥才能解开，两把钥匙在数学上是关联的，但你拿着公钥反推私钥就走不通了。'
                  '这条结构把对称加密多年来一直没解决干净的"密钥分发"问题给绕过去了——公钥可以随便扔到任何信道上去广播，'
                  '私钥从头到尾不出本地；哪怕公钥被攻击者全收走了，密文对他们仍是黑箱。')
    add_para(doc, '但裸 RSA 是不能直接拿来用的。早年那种 PKCS #1 v1.5 填充方式，在选择密文攻击下早就被研究透了，'
                  '现代实现必须换成更稳的填充——也就是 OAEP。它在加密过程里塞了一个随机数和一个掩码生成函数（MGF），'
                  '同一段明文连续加密两遍，得到的密文也不会一样，"选择密文攻击"在原理上就被堵掉了。'
                  '本系统具体用的是 RSA-OAEP-SHA256：SHA-256 在这里同时承担两份职责——'
                  '既作为 MGF 的内部哈希，也用来哈希 label 字段；整体配置和当前业界对 RSA 的推荐参数是一致的。')
    add_para(doc, 'Web Crypto API 是 W3C 规范里的浏览器原生加密接口，'
                  '提供了密钥生成、对称/非对称加解密、签名、哈希这些基础原语。'
                  '最关键的一点是——所有运算都在浏览器进程内部完成，'
                  '不依赖任何第三方 JS 加密库。'
                  '这意味着两件事：性能比纯 JS 实现高出至少一个数量级，'
                  '安全性也更可靠，因为算法实现来自浏览器厂商，'
                  '而不是某个随手安装的 npm 包。'
                  '本系统调用的就是 window.crypto.subtle，'
                  '由前端直接完成 RSA-2048 密钥对的生成、导入导出与消息加解密。')

    add_heading(doc, '2.7  MySQL 数据库与 GORM', level=2)
    add_para(doc, 'MySQL 几乎是开源关系型数据库的代名词，'
                  '生态成熟、运维成本低、性能足够撑起绝大多数中小型 Web 应用。'
                  '本系统采用 MySQL 8.x，'
                  '设计了 users、friend_requests、friends、messages 四张核心数据表，'
                  '覆盖账号、好友申请、好友关系与消息四个领域。')
    add_para(doc, 'GORM 则是 Go 生态里最流行的 ORM，'
                  '提供链式 API、自动迁移、关联查询和事务管理，'
                  '把大部分原生 SQL 工作收敛到结构体声明里。'
                  '本系统在 Repository 层使用 GORM 处理数据访问，'
                  '通过 struct tag 显式声明字段映射，'
                  '在开发效率与 SQL 可控性之间留了一条退路——'
                  '复杂查询需要时也可以直接写 Raw SQL。')
    add_page_break(doc)
