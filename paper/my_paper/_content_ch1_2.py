def write_ch2(doc):
    add_heading(doc, '2  相关技术介绍', level=1)

    add_heading(doc, '2.1  前后端分离架构', level=2)
    add_para(doc, '前后端分离的本质是把原本耦合在一起的视图与业务拆开：'
                  '前端工程跑在浏览器里，专心处理渲染、交互与状态；'
                  '后端工程跑在服务器上，只负责业务逻辑、数据持久化和接口暴露。'
                  '两边通过 RESTful JSON 接口与 WebSocket 长连接对话。'
                  '相比传统服务端渲染，这种结构在团队协作、客户端复用与水平扩展方面都更友好——'
                  '前端、后端可以各自迭代，'
                  '同一套接口既能服务 Web，也能复用到移动端或桌面端。'
                  '本系统的前端用 Vue 3，后端用 Go + Gin，'
                  '整体走的就是这一主流路线。')

    add_heading(doc, '2.2  Vue 3 与前端技术栈', level=2)
    add_para(doc, 'Vue 3 是一款响应式驱动的渐进式前端框架。'
                  '相较于 Vue 2，它带来了 Composition API、'
                  '基于 Proxy 的新响应式系统，以及对 TypeScript 的一等支持。'
                  '本系统以 Vue 3 为核心，'
                  '搭配 Vite（基于 ESM 的构建工具，冷启动几乎瞬时）、'
                  'TypeScript（静态类型，能在编译期挡掉大量低级错误）、'
                  'Pinia（官方推荐的状态管理，接口比 Vuex 简洁、类型推断更友好）、'
                  'Vue Router（前端路由 + 全局守卫做登录拦截）'
                  '以及 Axios（HTTP 客户端，配合拦截器自动塞入 JWT 并统一处理错误）一起使用，'
                  '构成完整的前端工具链。')

    add_heading(doc, '2.3  Go 语言与 Gin 框架', level=2)
    add_para(doc, '后端选 Go 的最直接原因是并发。'
                  'IM 单机要同时维护大量 WebSocket 长连接，'
                  '而 Go 的 goroutine 加 channel 几乎是为这种场景量身定制——'
                  '每个连接对应一个轻量 goroutine，'
                  '资源开销远低于线程，调度也由运行时自动接管。'
                  '此外，Go 语法简洁、编译产物是单一二进制，部署体验极佳。')
    add_para(doc, 'Gin 是 Go 生态里使用最多的 Web 框架。'
                  '它在 net/http 之上做了 Radix Tree 路由优化，'
                  '并提供一套优雅的中间件机制——'
                  '本系统正是借此把 CORS、JWT 鉴权、日志统一塞到中间件层，'
                  '业务 Handler 里几乎看不到模板代码。'
                  'WebSocket 升级请求也走同一套路由系统，'
                  'HTTP 与 WS 在工程上不需要两套基础设施。')

    add_para(doc, '另一个值得说一下的点是 Go 的工程文化。'
                  'gofmt、go vet、go test 这三件套几乎规定了代码风格、'
                  '静态检查与单元测试的最低水位线。'
                  '团队协作时不会出现"花一上午争论花括号该不该换行"这种事——'
                  '这在多人毕业设计或工程实践里其实非常省心。')

    add_heading(doc, '2.4  WebSocket 协议', level=2)
    add_para(doc, '在 WebSocket 出现之前，'
                  '浏览器想做"准实时"通信，'
                  '只能靠 Comet 长轮询或者 HTTP 流——'
                  '前者频繁建连销毁，'
                  '后者难以穿透代理。'
                  'WebSocket 用一次握手把这个老问题彻底解决。')
    add_para(doc, 'WebSocket 由 RFC 6455 定义，是一种基于 TCP 的全双工协议。'
                  '与 HTTP 的"一次请求—一次响应"不同，'
                  'WebSocket 在握手完成后会保持长连接，双向都能随时推送消息，'
                  '正好契合 IM、在线协作、推送通知这类场景。'
                  '它的握手阶段复用 HTTP，'
                  '通过 "Upgrade: websocket" 头把 HTTP 连接转成 WebSocket，'
                  '之后的字节流就不再受 HTTP 模型约束了。')
    add_para(doc, '本系统用 WebSocket 来承载实时下行：'
                  '客户端通过 GET /ws?token=XXX 携带 JWT 完成握手，'
                  '服务端校验后将连接升级为 WS 并注册到统一的 Hub。'
                  '当 Hub 收到某条新消息时，按接收方 ID 查找活跃连接并下发密文。'
                  '后端用的 Gorilla WebSocket 是 Go 生态事实上的标准实现，'
                  '比标准库稳定不少。')

    add_heading(doc, '2.5  JWT 身份认证', level=2)
    add_para(doc, 'JWT（JSON Web Token）由 RFC 7519 定义，'
                  '是一种紧凑、自包含的令牌格式，'
                  '在前后端分离系统里通常用来替代 Session。'
                  '它由 Header、Payload、Signature 三段组成，'
                  '中间用点号连接并做 Base64URL 编码。'
                  'Payload 里可以塞用户 ID、过期时间等业务字段，'
                  'Signature 则保证整串令牌没被篡改。')
    add_para(doc, '相比 Session，JWT 的吸引力在于服务端不需要维护会话状态——'
                  '这一点对横向扩展尤其友好；'
                  '同一份令牌天然适用于 Web、移动端等多客户端；'
                  '业务上下文写进 Payload 还能省掉一次数据库查询。'
                  '本系统登录成功后由后端签发 JWT，'
                  '前端缓存在 localStorage，'
                  'Axios 拦截器自动加到请求头，'
                  '后端中间件统一校验。'
                  'WebSocket 握手时改走 URL 查询参数，鉴权逻辑保持一致。')

    add_heading(doc, '2.6  RSA-OAEP 公钥加密与 Web Crypto API', level=2)
    add_para(doc, 'RSA 是当今应用最广的非对称加密算法，'
                  '由 Rivest、Shamir、Adleman 三位学者于 1977 年提出，'
                  '安全性建立在"大整数分解困难"这一假设之上。'
                  '它的精妙之处在于一对密钥：'
                  '公钥负责加密，私钥负责解密，'
                  '两者数学上相关却无法相互推导。'
                  '这一性质把困扰对称加密多年的密钥分发问题一笔勾销——'
                  '公钥可以在任何信道上公开传播，'
                  '私钥则始终留在本地，'
                  '攻击者哪怕截获了公钥，也对密文无可奈何。')
    add_para(doc, '不过裸用 RSA 是危险的。'
                  'PKCS #1 v1.5 填充在选择密文攻击下早已被攻破，'
                  '因此现代实现必须配合更安全的填充模式——OAEP。'
                  'OAEP 引入随机数和 MGF 掩码生成函数，'
                  '让相同明文每次加密出的密文都不同，从根本上挫败选择密文攻击。'
                  '本系统选用的就是 RSA-OAEP-SHA256：'
                  'SHA-256 同时充当 MGF 的内部哈希和 label 的哈希，'
                  '安全强度符合当前业界对 RSA 的推荐配置。')
    add_para(doc, 'Web Crypto API 是 W3C 规范的浏览器原生加密接口，'
                  '提供密钥生成、对称/非对称加密、签名、哈希等基础原语。'
                  '关键的是，所有运算都在浏览器进程内执行，'
                  '不需要引入任何第三方 JS 加密库。'
                  '这一点意味着两件事：性能比纯 JS 实现高出数量级；'
                  '安全性也更可靠——因为算法实现来自浏览器厂商而非随便一个 npm 包。'
                  '本系统调用的就是 window.crypto.subtle，'
                  '在前端直接完成 RSA-2048 密钥对的生成、导入导出与消息加解密。')

    add_heading(doc, '2.7  MySQL 数据库与 GORM', level=2)
    add_para(doc, 'MySQL 几乎是开源关系型数据库的代名词，'
                  '生态成熟、运维成本低、性能足够撑起绝大多数中小型 Web 应用。'
                  '本系统采用 MySQL 8.x，'
                  '设计了 users、friend_requests、friends、messages 四张核心数据表，'
                  '覆盖账号、好友申请、好友关系与消息四个领域。')
    add_para(doc, 'GORM 则是 Go 生态里最流行的 ORM，'
                  '提供链式 API、自动迁移、关联查询和事务管理，'
                  '把大部分原生 SQL 工作收敛到结构体声明里。'
                  '本系统在 Repository 层使用 GORM 处理数据访问，'
                  '通过 struct tag 显式声明字段映射，'
                  '在开发效率与 SQL 可控性之间留了一条退路——'
                  '复杂查询需要时也可以直接写 Raw SQL。')
    add_page_break(doc)
